/**
 * Renders one or more JSON-LD blocks. Server component — safe to embed
 * schema objects built in src/lib/schema.ts. Schema must match visible
 * page content.
 */
export function JsonLd({ data }: { data: object | object[] }) {
  const blocks = Array.isArray(data) ? data : [data];
  return (
    <>
      {blocks.map((block, i) => (
        <script
          key={i}
          type="application/ld+json"
          dangerouslySetInnerHTML={{ __html: JSON.stringify(block) }}
        />
      ))}
    </>
  );
}
